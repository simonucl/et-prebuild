# -----------------------------------------------------------------------------
# Helpers
# -----------------------------------------------------------------------------
def read_lines(path: pathlib.Path):
    """
    Return the file’s contents as a list of lines *without*
    trailing newline characters.  Leading/trailing whitespace
    (other than newlines) is left intact to allow byte-for-byte
    comparison.
    """
    with path.open("r", encoding="utf-8") as fp:
        # .splitlines() removes the newline chars but keeps
        # blank lines as empty strings in the returned list.
        return fp.read().splitlines()


