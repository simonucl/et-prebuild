def test_raw_directory_and_files_exist():
    assert RAW_DIR.exists(), f"Expected directory {RAW_DIR} to exist."
    assert RAW_DIR.is_dir(), f"{RAW_DIR} exists but is not a directory."

    # Individual CSV presence
    for csv_path in (AWS_CSV, GCP_CSV, AZURE_CSV):
        assert csv_path.exists(), f"Expected file {csv_path} to be present."
        assert csv_path.is_file(), f"{csv_path} exists but is not a regular file."


@pytest.mark.parametrize(
    "csv_path,expected_lines",
    [
        (AWS_CSV, AWS_EXPECTED),
        (GCP_CSV, GCP_EXPECTED),
        (AZURE_CSV, AZURE_EXPECTED),
    ],
)
def test_raw_csv_contents(csv_path, expected_lines):
    actual = read_lines(csv_path)
    assert (
        actual == expected_lines
    ), f"Contents of {csv_path} differ from the expected template."


def test_no_destination_directories_exist_yet():
    assert not DEST_DIR.exists(), (
        f"Destination directory {DEST_DIR} already exists BEFORE the task starts. "
        "Student is expected to create it."
    )
    assert not ARCHIVE_DIR.exists(), (
        f"Archive directory {ARCHIVE_DIR} already exists BEFORE the task starts. "
        "Student is expected to create it."
    )


def test_no_output_files_exist_yet():
    """Ensure cost_summary.txt and run.log (and the CSV copies) are absent."""
    summary = DEST_DIR / "cost_summary.txt"
    runlog = DEST_DIR / "run.log"

    for output in (summary, runlog):
        assert not output.exists(), (
            f"File {output} already exists before the task starts. "
            "It should be generated by the student."
        )

    # Also ensure there are no CSVs in the prospective dest dir
    # (they’ll be copied there by the student).
    if DEST_DIR.exists():
        csvs_in_dest = list(DEST_DIR.glob("*_july2023.csv"))
        assert (
            not csvs_in_dest
        ), f"CSV files {csvs_in_dest} already found in {DEST_DIR}; none should exist yet."


def test_raw_directory_contains_only_expected_csvs():
    """
    For safety, verify there are no extra *_july2023.csv files
    that could interfere with downstream logic.
    """
    expected_names = {
        "aws_july2023.csv",
        "gcp_july2023.csv",
        "azure_july2023.csv",
    }
    actual_names = {p.name for p in RAW_DIR.glob("*_july2023.csv")}
    unexpected = actual_names - expected_names
    assert not unexpected, (
        f"Unexpected CSV files found in {RAW_DIR}: {sorted(unexpected)}. "
        "Only the three specified files should be present at start."
    )