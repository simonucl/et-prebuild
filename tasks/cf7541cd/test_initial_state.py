# test_initial_state.py
#
# This test-suite validates that the operating-system / filesystem is in the
# expected *initial* state, i.e. *before* the student starts solving the task.
# It checks:
#   • the presence and exact contents of the raw log files;
#   • that the summary artefacts do *not* yet exist.
#
# If any of these tests fail it means the exercise sandbox was not provisioned
# correctly or somebody has already started working on the task.

