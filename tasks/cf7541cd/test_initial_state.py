# ---------- Helper ------------------------------------------------------------

def read_file(path: Path) -> str:
    """
    Read a UTF-8 text file exactly as is.
    """
    with path.open("r", encoding="utf-8") as fh:
        return fh.read()

# ---------- Tests -------------------------------------------------------------

def test_raw_directory_exists():
    assert RAW_DIR.is_dir(), (
        f"Expected directory {RAW_DIR} to exist. "
        "It should contain the raw API log files shipped with the exercise."
    )


@pytest.mark.parametrize("path", [RAW_FILE_1, RAW_FILE_2])
def test_each_raw_file_exists(path: Path):
    assert path.is_file(), f"Missing required raw log file: {path}"


def test_raw_file_contents_are_exact():
    """
    The initial fixture files must match *exactly*; otherwise the statistical
    checks performed later will be meaningless.
    """
    mismatches = []
    for path, expected in EXPECTED_RAW_CONTENT.items():
        if not path.is_file():
            mismatches.append(f"{path} is missing")
            continue
        actual = read_file(path)
        if actual != expected:
            mismatches.append(
                f"Content mismatch in {path}:\n"
                "---- Expected ----\n"
                f"{expected}"
                "---- Actual ----\n"
                f"{actual}"
            )
    assert not mismatches, ";\n".join(mismatches)


def test_no_summary_directory_or_files_yet():
    """
    At the very beginning the summary artefacts must *not* be present.
    The student is supposed to create them.
    """
    assert not SUMMARY_CSV.exists(), (
        f"{SUMMARY_CSV} already exists — the summary must be generated by the student."
    )
    assert not SUMMARY_ERRORS.exists(), (
        f"{SUMMARY_ERRORS} already exists — the summary must be generated by the student."
    )

    # The summary directory itself may or may not exist; if it does exist it
    # must be empty to avoid confusion.
    if SUMMARY_DIR.exists():
        unexpected = [p for p in SUMMARY_DIR.iterdir() if p.name not in {".gitkeep"}]
        assert (
            not unexpected
        ), f"The directory {SUMMARY_DIR} should be empty at the start, found: {unexpected}"


def test_only_expected_raw_files_present():
    """
    The raw directory should contain exactly the two fixture files — nothing more,
    nothing less — so that later aggregation is deterministic.
    """
    expected_names = {RAW_FILE_1.name, RAW_FILE_2.name}
    actual_names   = {p.name for p in RAW_DIR.iterdir() if p.is_file()}

    extra   = actual_names - expected_names
    missing = expected_names - actual_names

    assert not extra,   f"Unexpected extra files in {RAW_DIR}: {sorted(extra)}"
    assert not missing, f"Missing files in {RAW_DIR}: {sorted(missing)}"